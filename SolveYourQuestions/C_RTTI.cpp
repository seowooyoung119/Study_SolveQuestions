/*
 * =========================================================================
 * [ C++ 런타임 타입 시스템 (RTTI) & 캐스팅 메커니즘 최종 정리 ]
 * =========================================================================
 * 
 * * 1. 객체의 3층 구조 (Memory Layout)
 * -------------------------------------------------------------------------
 * 
 * 🏢 1층 [현장]: 객체 인스턴스 (Memory Instance)
 * - 모든 다형성 객체의 맨 앞 8바이트는 [vptr](가상 함수 테이블 포인터)임.
 * - 이 주소는 모든 정보 탐색의 시작점(Entry Point).
 * 
 * * 📁 2층 [관리]: 가상 함수 테이블 (vtable) & 로케이터 (Locator)
 * - vtable: 함수 주소들의 목록.
 * - vtable[-1]: [CompleteObjectLocator]라는 신분증 포탈이 숨겨져 있음.
 * - Locator는 프로그램 시작점(ImageBase)으로부터의 상대적 거리(Offset)를 통해
 * 보안 환경(ASLR)에서도 진짜 데이터 위치를 찾아냄.
 * 
 * * 📜 3층 [보관]: RTTI 데이터 (Identity)
 * - TypeDescriptor: 클래스의 진짜 이름(Mangling Name, 예: .?AVChild@@).
 * - ClassHierarchyDescriptor: 상속 계보(Ancestry)와 다중 상속 위치 정보 보관.
 * 
 * -------------------------------------------------------------------------
 * * 2. 캐스팅의 철학 (Casting Philosophy)
 * -------------------------------------------------------------------------
 * ▶ 업캐스팅 (Up-casting)
 * - 부모의 포인터로 자식을 가리키는 행위.
 * - 컴파일러가 설계도(코드) 상에서 이미 관계를 알기 때문에 검사 생략.
 * - 런타임 비용이 거의 없음 (static_cast와 동일한 속도).
 * 
 * * ▶ 다운캐스팅 & 크로스 캐스팅 (Down/Cross-casting)
 * - dynamic_cast를 통해 "정말 이 족보에 해당 이름이 있는지" 확인.
 * - 런타임에 족보 리스트를 전수 조사하므로 성능 비용이 발생함.
 * - 실패 시 nullptr을 반환하여 프로그램이 터지는 것을 막는 안전장치.
 * 
 * -------------------------------------------------------------------------
 * * 3. 인터페이스 캐스팅의 정체 (Interface Casting)
 * -------------------------------------------------------------------------
 * 
 * - C++ 인터페이스는 '순수 가상 함수만 가진 클래스'임.
 * - 다중 상속 구조에서 인터페이스를 상속받으면, 객체 내부에 별도의 vptr이 생김.
 * - 캐스팅 시 RTTI는 단순히 이름만 확인하는 게 아니라, 해당 인터페이스의
 * vtable이 시작되는 위치로 [포인터 주소를 점프(Offset Adjustment)] 시킴.
 * - 결과적으로 "포인터 주소는 같아도 타입은 다를 수 있다"는 상식을 깨고,
 * "타입에 따라 포인터 주소도 변할 수 있다"는 원리가 적용됨.
 * 
 * -------------------------------------------------------------------------
 * * 4. 최종 요약: 왜 이걸 알아야 하는가?
 * -------------------------------------------------------------------------
 * 
 * - 성능 최적화: dynamic_cast가 왜 무거운지 이해하고 꼭 필요할 때만 쓸 수 있음.
 * - 타입 안전성: 런타임에 정체를 숨길 수 없는 근거(RTTI)를 알고 안전한 코드 설계 가능.
 * - 언리얼/유니티 이해: 대형 엔진들의 캐스팅 시스템이 이 표준 RTTI 기반 위에
 * 성능을 더하기 위해 자체적인 캐스팅 꼼수를 쓴다는 것을 이해하는 토대가 됨.
 * 
 * =========================================================================
 */