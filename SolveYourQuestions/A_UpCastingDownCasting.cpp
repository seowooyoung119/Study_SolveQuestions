/*
 * [ C++ 런타임 타입 정보(RTTI) 및 캐스팅 원리 요약 ]
 * * 1. 캐스팅의 종류와 작동 방식
 * -------------------------------------------------------------------------
 * 
 * - static_cast :
 * "컴파일 타임"에 형변환을 결정합니다.
 * 별도의 신분증 확인 절차 없이 주소값만 믿고 넘겨주므로 매우 빠르지만,
 * 잘못된 타입으로 변환 시 메모리 오염(Crash) 위험이 있습니다.
 * 
 * * - dynamic_cast :
 * "런타임"에 실제 객체의 신분증을 전수 조사합니다.
 * 상속 계보를 모두 뒤져서 안전이 확인될 때만 주소를 반환하고,
 * 실패하면 nullptr을 반환하는 안전장치가 핵심입니다.
 * 
 * -------------------------------------------------------------------------
 * * 2. C++ 타입 시스템의 3층 구조 (메모리 내부 레이아웃)
 * -------------------------------------------------------------------------
 * 
 * [🏢 1층: 객체의 메모리 (현장)]
 * - vptr (가상 함수 테이블 포인터):
 * 객체 맨 앞에 위치한 8바이트 주소입니다. 모든 정보 탐색의 시작점입니다.
 * 
 * * [📁 2층: vtable 및 Locator (중앙 관리소)]
 * - vtable: 가상 함수들의 주소가 모여 있는 배열입니다.
 * - CompleteObjectLocator (vtable[-1]):
 * vtable 바로 앞에 숨겨진 신분증 데이터베이스로 가는 '포탈'입니다.
 * 
 * * [📜 3층: RTTI 데이터 (신분증 보관소)]
 * - TypeDescriptor: 클래스의 진짜 이름 (예: .?AVChild@@)이 적힌 명찰입니다.
 * - ClassHierarchyDescriptor: 상속 계보(누가 누구를 상속받았는지)가 적힌 족보입니다.
 * 
 * -------------------------------------------------------------------------
 * * 3. dynamic_cast의 5단계 정체 확인 프로세스
 * -------------------------------------------------------------------------
 * 
 * Step 1. 객체의 머리에 있는 [vptr]을 확인합니다.
 * Step 2. vptr을 타고 올라가 [vtable[-1]] 위치에 있는 [Locator]로 점프합니다.
 * Step 3. Locator가 가리키는 [Hierarchy(계보 총람)]를 펼칩니다.
 * Step 4. 계보 리스트([0]번부터 끝까지)를 순회하며 내가 변환하려는 클래스 이름
 * (예: .?AVParent@@)이 명단에 있는지 문자열 대조를 수행합니다.
 * Step 5. [일치 발견] -> 해당 메모리 주소 반환 (성공)
 * [미발견]   -> nullptr 반환 (실패/안전)
 * 
 * -------------------------------------------------------------------------
 * 
 * * 4. 결론 및 통찰
 * - 성능: dynamic_cast는 메모리를 여러 번 참조하고 루프를 돌기 때문에 성능 비용이 발생합니다.
 * - 다형성: 포인터가 부모 타입이라도 실제 몸통(메모리) 안에 신분증이 박혀 있으므로
 * 런타임에 정체를 정확히 파악할 수 있는 것입니다.
 */